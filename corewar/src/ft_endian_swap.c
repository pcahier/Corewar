#include "corewar.h"

uint32_t	ft_uns_swaptobig(uint32_t num)
{
	uint32_t	swap;

	swap = ((num >> 24) & 0xff) | ((num << 8) & 0xff0000)
		| ((num >> 8) & 0xff00) | ((num << 24) & 0xff000000);
	return (swap);
}
